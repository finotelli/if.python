# neste programa estou aprendendo como aplicar o if e else
a = int(input('Digite um valor numérico: '))
if (a < 10) :
    print('Você digitou um número menor que 10.')
else :
    print('Você digitou um número maior ou igual a 10. ')
print('FIM DO PROGRAMA')

# no código acima, se o número que a pessoa digitar no terminal for menor que 10, resultará na mensagem "voce digitou um numero menor que 10"
# e finalizará finalizando o programa com a mensagem avisando.
# já se o numero digitado for maior/igual a 10, o programa exibirá uma mensagem avisando que o numero é igual/maior que 10.
# por fim, finalizará o programa.